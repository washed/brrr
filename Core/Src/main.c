void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim) {
	static uint8_t first_capture = 0;
	if (htim == FREQ_IC_TIM_INSTANCE_PTR) {
		if (first_capture == 0) {
			captured_count_1 = HAL_TIM_ReadCapturedValue(&htim3, TIM_CHANNEL_2);
			first_capture = 1;
		} else if (first_capture == 1) {
			captured_count_2 = HAL_TIM_ReadCapturedValue(&htim3, TIM_CHANNEL_2);

			if (captured_count_2 > captured_count_1) {
				captured_period = (captured_count_2 - captured_count_1) * 2; // us
				captured_frequency = 1000000 / captured_period; // Hz
			}
			first_capture = 0;
		}
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	if (htim == UI_UPDATE_TIM_INSTANCE_PTR)
		ui_update_flag = 1;

	if (htim == LVGL_TICK_TIM_INSTANCE_PTR)
		lv_tick_inc(1);
}

